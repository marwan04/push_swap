#include "libft.h"

char	*ft_strchr(const char *s, int i)
{
	unsigned char	c;

	c = (unsigned char) i;
	while (*s)
	{
		if (*s == c)
			return ((char *)s);
		s++;
	}
	if (c == '\0')
		return ((char *)s);
	return (0);
}
